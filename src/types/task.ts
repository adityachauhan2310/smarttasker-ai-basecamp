export type TaskStatus = 'todo' | 'in-progress' | 'done';
export type TaskPriority = 'low' | 'medium' | 'high';

export interface Task {
  id: string;
  title: string;
  description: string;
  status: TaskStatus;
  priority: TaskPriority;
  dueDate: string;
  createdAt: string;
  userId?: string;
  updatedAt?: string;
}

export interface TaskWithDependencies extends Task {
  dependencies?: Task[];
  dependents?: Task[];
} 